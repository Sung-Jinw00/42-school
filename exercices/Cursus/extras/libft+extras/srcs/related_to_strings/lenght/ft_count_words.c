#include "libft_extras.h"

/**
 * @return
 * The number of strings in an array of strings.
*/
int	ft_count_words(const char **split)
{
	int	i;

	i = 0;
	if (!split)
		return (0);
	while (split[i])
		i++;
	return (i);
}
